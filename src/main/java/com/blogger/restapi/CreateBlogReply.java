package com.blogger.restapi;

public class CreateBlogReply {
	
	String title;
	String data;
    String label;
	String createBlogStatus;
    public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public String getCreateBlogStatus() {
		return createBlogStatus;
	}
	public void setCreateBlogStatus(String createBlogStatus) {
		this.createBlogStatus = createBlogStatus;
	}
	
	  

}
